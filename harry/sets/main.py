# Sets are unordered collection of data items. They store items in a single variable. Set items are separated by commas and enclosed within curly brackets{}. Sets are unchangable, meaning you cannot change items of the set once created. Sets do not contain duplicate items.

thisSets = {2,3,5,6,2,3}
print(thisSets)

